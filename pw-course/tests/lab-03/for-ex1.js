// Tính tổng từ 1 đến 100

// Khai bao bien de luu tong
let sum = 0;

// Su dung vong lap for de tinh tong tu 1 den 100
for (let i = 0; i <= 100; i++) {
    sum += i;   // cong gia tri cua i vao bien sum
}

// In ra tong cua cac so tu 1 den 100

console.log(" Tong cua cac so tu 1 den 100 la: " + sum);